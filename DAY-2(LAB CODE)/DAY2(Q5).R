# Create a numeric vector
num_vec <- c(1, 2, 3)
print(num_vec)
print(class(num_vec))

# Create a character vector
char_vec <- c("apple", "banana", "cherry")
print(char_vec)
print(class(char_vec))

# Create a logical vector
logic_vec <- c(TRUE, FALSE, TRUE)
print(logic_vec)
print(class(logic_vec))